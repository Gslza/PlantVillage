def build_transfer_learning(input_shape=(128, 128, 3), num_classes=10, trainable=False):
    """
    Transfer Learning MobileNetV2 + Classification Head:
    - Base: MobileNetV2 (imagenet), layer beku untuk feature extraction
    - Head: GlobalAveragePooling2D → Dense(256) → Dropout → Output
    """
    base = MobileNetV2(
        input_shape = input_shape,
        include_top = False,
        weights     = 'imagenet'
    )
    base.trainable = trainable  # False = freeze semua layer

    inputs = keras.Input(shape=input_shape)
    x = base(inputs, training=False)
    x = layers.GlobalAveragePooling2D()(x)
    x = layers.Dense(256, activation='relu')(x)
    x = layers.BatchNormalization()(x)
    x = layers.Dropout(0.40)(x)
    outputs = layers.Dense(num_classes, activation='softmax')(x)

    model = keras.Model(inputs, outputs, name='MobileNetV2_TL')
    return model, base

