def predict_image(img_path, model, class_names, img_size=IMG_SIZE, top_k=3):
    """
    Inferensi satu gambar dan tampilkan:
    - Gambar uji
    - Label asli (dari nama folder)
    - Hasil prediksi & probabilitas top-k
    """
    img = Image.open(img_path).convert('RGB').resize(img_size)
    arr = np.array(img) / 255.
    arr = np.expand_dims(arr, axis=0)

    probs = model.predict(arr, verbose=0)[0]
    top_indices = np.argsort(probs)[::-1][:top_k]

    true_label = Path(img_path).parent.name
    pred_label = class_names[top_indices[0]]
    pred_prob  = probs[top_indices[0]]

    return img, true_label, pred_label, pred_prob, top_indices, probs


