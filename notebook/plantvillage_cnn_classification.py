def evaluate_model(model, test_gen, class_names, title='Model'):
    """Evaluasi lengkap: akurasi, confusion matrix, classification report."""
    test_gen.reset()
    preds  = model.predict(test_gen, verbose=0)
    y_pred = np.argmax(preds, axis=1)
    y_true = test_gen.classes

    loss, acc = model.evaluate(test_gen, verbose=0)
    print(f"\n{'='*55}")
    print(f"  EVALUASI — {title}")
    print(f"{'='*55}")
    print(f"  Test Loss    : {loss:.4f}")
    print(f"  Test Accuracy: {acc*100:.2f}%")
    print(f"{'='*55}")

    # Classification Report
    print("\nClassification Report:")
    print(classification_report(y_true, y_pred, target_names=class_names))

    # Confusion Matrix
    cm = confusion_matrix(y_true, y_pred)
    fig, ax = plt.subplots(figsize=(12, 10))
    disp = ConfusionMatrixDisplay(cm, display_labels=class_names)
    disp.plot(ax=ax, colorbar=True, cmap='Blues', xticks_rotation=45)
    ax.set_title(f'Confusion Matrix — {title}', fontsize=13, fontweight='bold')
    plt.tight_layout()
    plt.savefig(f'cm_{title.lower().replace(" ","_")}.png', dpi=150, bbox_inches='tight')
    plt.show()

    return loss, acc, y_true, y_pred


def analyze_fit(train_acc, val_acc, test_acc, model_name):
    """Analisis overfitting / underfitting / good fit."""
    gap = abs(train_acc - val_acc)
    print(f"\n📊 Analisis Kesesuaian Model — {model_name}")
    print(f"   Train Acc   : {train_acc*100:.2f}%")
    print(f"   Val Acc     : {val_acc*100:.2f}%")
    print(f"   Test Acc    : {test_acc*100:.2f}%")
    print(f"   Gap (|T-V|) : {gap*100:.2f}%")
    if train_acc < 0.80:
        status = '⚠️  UNDERFITTING — Model belum belajar dengan baik'
    elif gap > 0.10:
        status = '⚠️  OVERFITTING — Model terlalu hafal data training'
    else:
        status = '✅  GOOD FIT — Model generalisasi dengan baik'
    print(f"   Status      : {status}")
    return status

